public class Node {

    int TREE_X;
    int TREE_Y;
    int TREE_CANOPY;

    /**
     * This is constructor that takes in three integers
     *
     * @param x
     * @param y
     * @param canopy
     */
    public Node(int x, int y, int canopy) {

        this.TREE_X = x;
        this.TREE_Y = y;
        this.TREE_CANOPY = canopy;
    }

}
